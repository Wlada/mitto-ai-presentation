#!/usr/bin/env node
// Read raw vitest coverage-final.json files and spec test counts, then write
// a typed RESULTS module that slide-06-results imports. Run after `npm run
// coverage` to refresh the numbers shown in the presentation.

import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(fileURLToPath(import.meta.url), '..', '..');

function summariseCoverage(coverageFinalPath) {
  if (!existsSync(coverageFinalPath)) {
    return null;
  }
  const data = JSON.parse(readFileSync(coverageFinalPath, 'utf8'));
  const totals = { lines: { covered: 0, total: 0 }, statements: { covered: 0, total: 0 }, functions: { covered: 0, total: 0 }, branches: { covered: 0, total: 0 } };
  for (const file of Object.values(data)) {
    for (const hits of Object.values(file.s ?? {})) {
      totals.statements.total += 1;
      if (hits > 0) totals.statements.covered += 1;
    }
    for (const hits of Object.values(file.f ?? {})) {
      totals.functions.total += 1;
      if (hits > 0) totals.functions.covered += 1;
    }
    for (const branchHits of Object.values(file.b ?? {})) {
      for (const hits of branchHits) {
        totals.branches.total += 1;
        if (hits > 0) totals.branches.covered += 1;
      }
    }
    const lineMap = new Map();
    for (const [stmtId, loc] of Object.entries(file.statementMap ?? {})) {
      const line = loc.start.line;
      const hits = file.s[stmtId] ?? 0;
      lineMap.set(line, (lineMap.get(line) ?? 0) + hits);
    }
    for (const hits of lineMap.values()) {
      totals.lines.total += 1;
      if (hits > 0) totals.lines.covered += 1;
    }
  }
  const pct = (covered, total) => (total === 0 ? 0 : Math.round((covered / total) * 1000) / 10);
  return {
    lines: pct(totals.lines.covered, totals.lines.total),
    statements: pct(totals.statements.covered, totals.statements.total),
    functions: pct(totals.functions.covered, totals.functions.total),
    branches: pct(totals.branches.covered, totals.branches.total),
  };
}

function countTests(specGlobRoots) {
  let total = 0;
  for (const root of specGlobRoots) {
    if (!existsSync(root)) continue;
    walk(root, (file) => {
      if (!file.endsWith('.spec.ts')) return;
      const src = readFileSync(file, 'utf8');
      const matches = src.match(/^\s*(?:it|test)\s*\(/gm);
      if (matches) total += matches.length;
    });
  }
  return total;
}

import { execSync } from 'node:child_process';

function countPlaywrightTests(e2eDir) {
  if (!existsSync(e2eDir)) return 0;
  try {
    const out = execSync('npx playwright test --list 2>&1', {
      cwd: e2eDir,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'pipe'],
    });
    const match = out.match(/Total:\s+(\d+)\s+tests/);
    if (match) return Number(match[1]);
  } catch {
    // Fall through to spec-file count.
  }
  return 0;
}

function walk(dir, visit) {
  for (const entry of readdirSync(dir)) {
    if (entry === 'node_modules' || entry.startsWith('.')) continue;
    const full = join(dir, entry);
    const st = statSync(full);
    if (st.isDirectory()) walk(full, visit);
    else visit(full);
  }
}

const webCoverage = summariseCoverage(join(repoRoot, 'apps/web/coverage/web/coverage-final.json'))
  ?? summariseCoverage(join(repoRoot, 'apps/web/coverage/coverage-final.json'));
const serverCoverage = summariseCoverage(join(repoRoot, 'server/coverage/coverage-final.json'));

const webTests = countTests([join(repoRoot, 'apps/web/src')]);
const serverTests = countTests([join(repoRoot, 'server/src'), join(repoRoot, 'server/tests')]);
// Playwright dynamic test generation (e.g. `for` loops) is not visible to a
// regex over spec files; ask Playwright itself for the real count.
const e2eTests =
  countPlaywrightTests(join(repoRoot, 'e2e')) || countTests([join(repoRoot, 'e2e/tests')]);

const generatedAt = new Date().toISOString().slice(0, 10);

const out = {
  webTests,
  serverTests,
  e2eTests,
  totalUnitTests: webTests + serverTests,
  webCoverage: webCoverage ? Math.floor(webCoverage.lines) : null,
  serverCoverage: serverCoverage ? Math.floor(serverCoverage.lines) : null,
  generatedAt,
};

const tsBody = `// Auto-generated by scripts/refresh-results.mjs. Do not edit by hand.
// Run: npm run results:refresh

export interface Results {
  readonly webTests: number;
  readonly serverTests: number;
  readonly e2eTests: number;
  readonly totalUnitTests: number;
  readonly webCoverage: number | null;
  readonly serverCoverage: number | null;
  readonly generatedAt: string;
}

export const RESULTS: Results = ${JSON.stringify(out, null, 2)} as const;
`;

const targetPath = join(repoRoot, 'apps/web/src/app/slides/slide-06-results/results.data.ts');
writeFileSync(targetPath, tsBody);

console.log(`Wrote ${targetPath}`);
console.log(JSON.stringify(out, null, 2));
